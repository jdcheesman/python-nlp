  def classify(self, words):
    """ TODO
      'words' is a list of words to classify. Return 'pos' or 'neg' classification.
    """

    if not self.totals_evaluated:
      total_docs = 0
      self.total_num_classes = len(self.class_count)
      for clazz in self.class_count:
        total_docs += self.class_count[clazz]
      for clazz in self.class_count:
        p_clazz = math.log((self.class_count[clazz]+0.0)/(total_docs+0.0))
        self.p_class[clazz] = p_clazz

      # don't evaluate twice!
      self.totals_evaluated = True

    # called after addExample
    # note that this is called repeatedly [(addExample*n) - (classify*m)] per fold.

    p_current_class = dict()
    for klass in self.class_count:
      probabilityClass = self.class_count[klass]
      vocab = self.all_classes[klass]

      for word in words:
        num_words = 1
        if word in vocab:
          num_words += vocab[word]

        cnt_word_all_classes = 0
        if word in self.all_word_count:
          cnt_word_all_classes = self.all_word_count[word]
          p_word = math.log((num_words+0.0) / (cnt_word_all_classes+self.total_vocab+0.0))
          probabilityClass += p_word

      p_current_class[klass] = probabilityClass

    result = ''
    current_max = -1
    for klass in p_current_class:
      if p_current_class[klass] > current_max:
        result = klass
        current_max = p_current_class[klass]
    return result


  def addExample(self, klass, words):
    """
     * TODO
     * Train your model on an example document with label klass ('pos' or 'neg') and
     * words, a list of strings.
     * You should store whatever data structures you use for your classifier
     * in the NaiveBayes class.
     * Returns nothing
    """

    # klass: pos or neg, text string
    # words: array of words in the given document.
    class_cnt = 1
    if klass in self.class_count:
      class_cnt += self.class_count[klass]
    self.class_count[klass] = class_cnt

    vocab = dict()
    if klass in self.all_classes:
      vocab = self.all_classes[klass]
    else:
      self.all_classes[klass] = vocab

    for word in words:
      if (not (self.FILTER_STOP_WORDS and word in self.stopList)):
        val = 1
        if word in vocab:
          if self.binarized:
            val = 1
          else:
            val += vocab[word]
        vocab[word] = val

        val = 1

        if word in self.all_word_count:
          if self.binarized:
            val = 1
          else:
             val += self.all_word_count[word]
        else:
          self.total_vocab += 1

        self.all_word_count[word] = val


